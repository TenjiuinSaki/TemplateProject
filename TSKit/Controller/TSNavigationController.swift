import UIKit

class TSNavigationController: UINavigationController {

    override func viewDidLoad() {
        super.viewDidLoad()
        
        // Do any additional setup after loading the view.
        
        navigationBar.isHidden = true
    }
    
    /// 显示新特性
    func showNewFeature(image: [TSAssets]) {
        let key = "AppVersion"
        let curVersion = Bundle.main.version
        
        if let version = ts.helper.getCache(key: key)?.value as? String {
            if version == curVersion {
                return
            }
        }
        
        let guideView = TSFeatureView(images: image)
        view.addSubview(guideView)
        guideView.full()
    }
    
    override var preferredStatusBarStyle: UIStatusBarStyle {
        
        return topViewController?.preferredStatusBarStyle ?? .default
    }
    
    override var shouldAutorotate: Bool {
        return true
    }
//    override var supportedInterfaceOrientations: UIInterfaceOrientationMask {
//        return .portrait
//    }
}

